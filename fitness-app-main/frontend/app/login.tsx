import React, { useState } from 'react';
import {
  View,
  Text,
  TextInput,
  TouchableOpacity,
  StyleSheet,
  KeyboardAvoidingView,
  Platform,
  ScrollView,
  ActivityIndicator,
  Alert,
} from 'react-native';
import { useRouter } from 'expo-router';
import { authAPI } from '../src/services/api';
import { useAuthStore } from '../src/store/authStore';
import { Ionicons } from '@expo/vector-icons';
import * as WebBrowser from 'expo-web-browser';
import Constants from 'expo-constants';
import * as SecureStore from 'expo-secure-store';

WebBrowser.maybeCompleteAuthSession();

export default function LoginScreen() {
  const router = useRouter();
  const { setUser, setSessionToken } = useAuthStore();
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [loading, setLoading] = useState(false);
  const [showPassword, setShowPassword] = useState(false);

  

const handleLogin = async () => {
  if (!email || !password) {
    Alert.alert('Error', 'Please enter email and password');
    return;
  }

  setLoading(true);
  try {
    const response = await authAPI.login({ email, password });
    const { session_token, user } = response.data;

    // ✅ Save token in both store and SecureStore
    await SecureStore.setItemAsync('session_token', session_token);
    const saved = await SecureStore.getItemAsync('session_token');
    console.log("✅ Token saved:", saved);

    await setSessionToken(session_token);
    setUser(user);

    // Navigate based on role
    if (user.role === 'head_admin') {
      router.replace('/admin');
    } else if (user.role === 'gym_manager') {
      router.replace('/manager');
    } else if (user.role === 'trainee') {
      router.replace('/trainee');
    } else if (user.role === 'trainer') {
      router.replace('/trainer');
    }
  } catch (error: any) {
    Alert.alert('Login Failed', error.response?.data?.detail || 'Invalid credentials');
  } finally {
    setLoading(false);
  }
};


  const handleGoogleLogin = async () => {
    try {
      const redirectUrl = Constants.expoConfig?.extra?.redirectUrl || 'https://app.emergent.sh';
      const authUrl = `https://auth.emergentagent.com/?redirect=${encodeURIComponent(redirectUrl)}`;
      
      const result = await WebBrowser.openAuthSessionAsync(authUrl, redirectUrl);
      
      if (result.type === 'success' && result.url) {
        const url = new URL(result.url);
        const sessionId = url.hash.replace('#session_id=', '');
        
        if (sessionId) {
          setLoading(true);
          const response = await authAPI.getSessionData(sessionId);
          const { session_token, ...userData } = response.data;
          
          await setSessionToken(session_token);
          setUser(userData);
          
          // Default OAuth users to trainee role
          router.replace('/trainee');
        }
      }
    } catch (error: any) {
      Alert.alert('OAuth Error', error.message || 'Failed to authenticate with Google');
    } finally {
      setLoading(false);
    }
  };

  return (
    <KeyboardAvoidingView
      style={styles.container}
      behavior={Platform.OS === 'ios' ? 'padding' : 'height'}
    >
      <ScrollView contentContainerStyle={styles.scrollContent}>
        <View style={styles.header}>
          <Ionicons name="fitness" size={80} color="#FF6B35" />
          <Text style={styles.title}>FitDesert</Text>
          <Text style={styles.subtitle}>Train. Track. Transform.</Text>
        </View>

        <View style={styles.form}>
          <View style={styles.inputContainer}>
            <Ionicons name="mail" size={20} color="#666" style={styles.inputIcon} />
            <TextInput
              style={styles.input}
              placeholder="Email"
              placeholderTextColor="#999"
              value={email}
              onChangeText={setEmail}
              keyboardType="email-address"
              autoCapitalize="none"
            />
          </View>

          <View style={styles.inputContainer}>
            <Ionicons name="lock-closed" size={20} color="#666" style={styles.inputIcon} />
            <TextInput
              style={styles.input}
              placeholder="Password"
              placeholderTextColor="#999"
              value={password}
              onChangeText={setPassword}
              secureTextEntry={!showPassword}
            />
            <TouchableOpacity onPress={() => setShowPassword(!showPassword)}>
              <Ionicons
                name={showPassword ? 'eye-off' : 'eye'}
                size={20}
                color="#666"
                style={styles.eyeIcon}
              />
            </TouchableOpacity>
          </View>

          <TouchableOpacity
            style={styles.loginButton}
            onPress={handleLogin}
            disabled={loading}
          >
            {loading ? (
              <ActivityIndicator color="#fff" />
            ) : (
              <Text style={styles.loginButtonText}>Login</Text>
            )}
          </TouchableOpacity>

          <View style={styles.divider}>
            <View style={styles.dividerLine} />
            <Text style={styles.dividerText}>OR</Text>
            <View style={styles.dividerLine} />
          </View>

          <TouchableOpacity
            style={styles.googleButton}
            onPress={handleGoogleLogin}
            disabled={loading}
          >
            <Ionicons name="logo-google" size={20} color="#fff" />
            <Text style={styles.googleButtonText}>Continue with Google</Text>
          </TouchableOpacity>

          <View style={styles.infoContainer}>
            <Ionicons name="shield-checkmark" size={16} color="#666" />
            <Text style={styles.infoText}>
              Secure access controlled by FitDesert HQ
            </Text>
          </View>
        </View>
      </ScrollView>
    </KeyboardAvoidingView>
  );
}

